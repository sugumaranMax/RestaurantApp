package com.example.myrestaurantapp.restaurant;

public class ReviewModel {
    private String name;
    private String date;
    private int rating;
    private String comments;

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public int getRating() {
        return rating;
    }

    public String getComments() {
        return comments;
    }
}