package com.example.myrestaurantapp.menu;

public class MenuItems {
    private int id;
    private String name;
    private String description;
    private String price;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getPrice() {
        return price;
    }
}
